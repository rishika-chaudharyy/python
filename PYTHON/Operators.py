#Arithmetic
print("Sum ",4+3)
print("Difference",4-3)
print("Product ",4*3)
print("Quotient ",4/3)
print("Remainder ",4%3)
print("Power ",4**3)
print("Floor quotient ",4//3)

#Assignment
a=5
b=a
print(a,b)
a+=b
print(a)

#Comparision
print(3==4)

#Logical
exp1=4>5
exp2=9>7
print(exp1 and exp2)
print(exp1 or exp2)
print(not(exp2))


#Identity
print(4 is 5)
print(4 is not 5)


#Membership
fruits=["rose","apple","Banana","cherry"]
print("banana" in fruits)


#Bitwise
print(13 & 17)
print(13 | 17)
print(13 ^ 17)
print(~13)